# 3.1.1 사칙연산
print(1 + 1)  # 2
print(3 - 2)  # 1
print(5 * 2)  # 10
print(6 / 3)  # 2


print(2**3)  # 2^3 = 8
print(10 % 3)  # 나머지 구하기 1
print(10 // 3)  # 몫 구하기 3

# 3.1.2 비교연산
print(10 > 3)  # True
print(4 >= 7)
print(10 < 3)
print(5 <= 5)


print(3 == 3)
print(4 == 2)
print(3 + 4 == 7)
print(1 != 3)

print((3 > 0) and (3 > 5))  # False
print((3 > 0) & (3 > 5))  # False

print((3 > 0) or (3 > 5))  # True
print((3 > 0) | (3 > 5))  # True

print(not (1 != 3))  # False

print(5 > 4 > 3)  # True
print(4 > 5 > 3)  # False
